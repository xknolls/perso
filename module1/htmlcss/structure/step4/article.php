        <section>

            <h2>Préparation</h2>

            <ol>
            <li>Déxortiquer les crevettes.</li>
            <li>Les faires revenir dans un peu d'huile d'olive, avec de l'ail écrasé et le thym émietté.</li>
            <li>Rajouter la boîte de bisque de homard.</li>
            <li>Délayer la bisque avec un peu d'eau si elle est épaisse.</li>
            <li>Incorporer "une pointe" de coulis de piment d'Espelette.</li>
            <li>Mélanger doucement.</li>
            <li>Goûter et rectifier selon votre goût avec sel, poivre et éventuellement du coulis (attention quand même !).</li>
            <li>Laisser cuire pendant une quinzaine de minutes.</li>
            <li>Servir chaud.</li>
            </ol>

        </section>
        
